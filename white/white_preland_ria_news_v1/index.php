            <div class="article__description">
              <p class="description__text">
                <span class="accent">ЕКАТЕРИНБУРГ, 20 фев - РИА Новости.</span
                >Мошенники начали активно атаковать российский бизнес,
                маскируясь под представителей ФНС и Федеральной службы судебных
                приставов (ФССП), сообщил журналистам директор департамента
                противодействия мошенничеству Сбербанка Сергей Велигодский.
              </p>
              <p class="description__text">
                <a href="" class="underline">"Сбер" </a>фиксирует всплеск
                мошеннических рассылок на электронную почту организаций -
                злоумышленники маскируются под представителей Федеральной
                налоговой службы и Федеральной службы судебных приставов", -
                сказал он в кулуарах форума "Кибербезопасность в финансах".
              </p>
              <p class="description__text">
                <a href="" class="underline">Велигодский</a> отметил, что за
                последние дни антифрод-система
                <a href="" class="underline">Сбербанка</a> вовремя выявила и
                приостановила все подозрительные операции в банке, поэтому
                ущерба на миллионы рублей удалось избежать полностью.
              </p>
              <p class="description__text">
                Он сообщил, что "Сбер" фиксирует подобные атаки и на
                аутсорсинговые компании, которые предоставляют бухгалтерские
                услуги сразу нескольким юридическим лицам и предпринимателям.
              </p>
              <p class="description__text">
                "Цель фишинговой рассылки - получение удаленного доступа к
                системам дистанционного банковского обслуживания и хищение денег
                со счетов компаний", - подчеркнул он.
              </p>
              <p class="description__text">
                По словам Велигодского, расследование показало, что незадолго до
                попыток мошеннических списаний на электронные адреса компаний
                приходили фишинговые письма якобы от
                <a href="" class="underline">ФНС</a> и
                <a href="" class="underline">ФССП</a>. Сообщения содержали файлы
                и ссылки с вредоносными программами.
              </p>
